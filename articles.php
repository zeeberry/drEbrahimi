					<h3><a href="index.html">Variation in Solving Programming Problems in an Online SUNY Learning Network Course</a></h3>
			
					<p class="post-info">By <a href="index.html">Dr. Ebrahimi</a> | <span class="date">April 20, 2006</span> </p>
				
					<p>An observation on teaching introductory programming courses on SLN for a period of two terms led me to believe that 
					on-line students try various ways to solve a problem. In the beginning, I got the impression that some of their solution
					approaches were wrong; but after a little investigation, I found that some of the problems solving approaches were correct
					but difficult to understand and not straight forward. However, some of the solutions were interesting, challenging, and 
					unique. Three problem sets will be discussed which were given from the textbook in modules 2 and 3 of the term. The 
					problems were to compute salary with overtime, compute total average mileage per gallon, and parking charge fee. As 
					an instructor who has taught introductory programming for more than two decades, I found several variations in solving 
					a problem, even in a small simple program like a compute salary program. One speculation would be that students use trial
					and error and their own logic to get the correct output, despite not using a common approach. It seems that students 
					in the face to face classes focus on the information from the instructor and disseminate it among each other.</p>			
				
