					<p>College and university web pages are an important forum for reaching out to prospective students and the school 
					community; these web pages also provide valuable information to the public [1,5]. This economical and convenient
					means of disseminating information, which is replacing traditional methods of communication by college catalogs,
					mail, and phone, has tremendous implications. As websites increasingly become a primary source of information, 
					extra care should be considered to avoid any crucial errors [4]. However, how accessible, accurate, helpful, 
					and up to date are college websites?</p>
			
					<p>
					This study investigates 21 Long Island universities and colleges and five institutions from other locations, 
					four of which are Ivy League. The web sites of these schools are analyzed based on criteria known as 7X2 C’s
					analysis in 7 separate layers of web application and development. The 7 pairs of C's are: (content, context),
					(correctness, credibility), (currency, continuity), (completeness, coverage), (consistency, conciseness), 
					(community, customization), and (compelling, creativity).</p>			
				
