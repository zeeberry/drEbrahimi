					<h3><a href="index.html">Design of a Web-based Visual Learning System (WVLS) via Plan Across Disciplines</a></h3>
			
					<p class="post-info">By <a href="index.html">Dr. Ebrahimi</a> | <span class="date">April 20, 2009</span></p>
				
					<p>WEB BASED LEARNING systems are becoming common ground for teaching and learning [Arnow, 99; Truong, 05]. A sound 
					design model has the potential to significantly increase the effectiveness of these systems. Teaching and learning 
					can be enhanced when a problem and its solution are visually decom- posed into plans and its sub-plans and subsequently 
					integrated. A plan is an abstraction that visually provides a solution to a problem or to a sub-problem in the sense, 
					it represents a problem from its macro level down to its micro level. Any important concept can be described as a plan
					and it is up to the educator and level of learner to decide on a particular plan. A plan can be visually represented by 
					a dot, a geometric shape or an image. The process of visualizing plan decomposition and then plan integration for a problem
					is demonstrated in WVLS – Web Visual Learn- ing System.</p>			
				
