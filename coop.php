<h3>C++ and Object Oriented Programming</h3>
<h4>Overview</h4>
<p>
<strong>Course offered at <a href="http://www.esc.edu/">SUNY Empire State College</a></strong>
</p>
<p>
This course deals with object-oriented programming (OOP) using C++. The main topics of discussion 
include C++ language features and the implementation of the OOP features of encapsulation, classes, 
inheritance, polymorphism and data hiding. C++ programming examples will be discussed and students 
will work on hands-on C++ programming assignments.
</p>
<p>
This is an online course. The term starts in March, May, September, November and January.
