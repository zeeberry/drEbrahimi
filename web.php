<h3>Web Systems & E-Commerce</h3>
<h4>Overview</h4>
<p>
<strong>Course offered at <a href="www.oldwestbury.edu/">SUNY Old Westbury</a></strong>
</p>
<p>
This course covers the basics of Web Systems, furthering an understanding of them through the 
development of e-commerce applications and systems. The course provides an overview of several
current web technologies, including client side applications, such as web design using JavaScript
and VBScript and server side applications on Apache (Linux) and IIS (Windows) using CGI, ASP and 
Java Servlet technologies. Students will implement an e-commerce application using the learned 
components involved in a web system and e-business strategies. Projects are carried out individually
and in groups.
</p>
<p>
At the end of the course, student will be able to apply the learned material in design of a high 
quality e-commerce system based on evaluating criteria known as 7x2C. (content, context), 
(correctness, credibility), (currency, continuity), (completeness, coverage), (consistency,
conciseness), (community, customization), and (compelling, creativity)
</p>
